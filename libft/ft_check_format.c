#include "libft.h"

int		ft_is_data_type(char c)
{
	if (c == 'c' || c == 'd' || c == 'i' || c == 's' || c == 'p' || c == 'x'
	|| c == 'X' || c == 'u' || c == '%' || c == 'n')
		return (1);
	return (0);
}

int		ft_is_flag(char c)
{
	if (c == '.' || c == '0' || c == '-' || c == '*' || ft_isdigit((int)c)
	|| c == '#' || c == '+' || c == ' ')
		return (1);
	return (0);
}
